/** Replace {key} placeholders in string with values from obj */
export function interpolate(str, obj = {}) {
  if (typeof str !== "string") return str;
  return str.replace(/\{(\w+)\}/g, (_, key) => (obj[key] != null ? String(obj[key]) : `{${key}}`));
}

/** Server or client: get t(locale, key, params?) */
export function getT(translationsMap) {
  return (locale, key, params) => {
    const localeMap = translationsMap[locale] || translationsMap.en;
    const raw = localeMap?.[key] ?? key;
    return interpolate(raw, params);
  };
}

/** Client: returns t(key, params) for a given locale */
export function getTForLocale(locale) {
  const localeMap = translations[locale] || translations.en;
  return (key, params) => interpolate(localeMap?.[key] ?? key, params);
}
